         <h4>ABSTRACT</h4>
         <p class="preview-text">
          When I wrote the first edition of Health Psychology over  30 years  ago, the task  was much simpler than it is now. Health psychology was a new field and was relatively small. In recent decades, the field has grown steadily, and great research advances have been made. Chief among these developments has been the use and refinement of the biopsychosocial model: the study of health issues from the stand- point of biological, psychological, and social factors acting together. Increasingly, research has attempted to identify the biological pathways by which psychosocial factors such as stress may adversely affect health and potentially protective factors such as social support may buffer the impact of stress. My goal in the tenth edition of this text is to convey this increasing sophistication of the field in a manner that makes it accessible, comprehensible, and exciting to undergraduates.
          
        </p>

         <h4>INTRODUCTION</h4>
         <p class="preview-text"> 
          Health psychology is an exciting and relatively new field devoted to understanding psychological influ-
          ences on how people stay healthy, why they become ill, and how they respond when they do get ill. Health psychologists both study such issues and develop in- terventions to help people stay well or recover from illness. For example, a health psychology researcher might explore why people continue to smoke even though they know that smoking increases their risk of cancer and heart disease. Understanding this poor health habit leads to interventions to help peo- ple stop smoking.
          Fundamental to research and practice in health psychology is the definition of health. Decades ago,  a forward-looking World Health Organization (1948) defined  health  as  “a  complete  state  of  physical,
          mental, and social well-being and not merely the ab-
          sence of disease or infirmity.” This definition is at  the core of health psychologists’ conception of health. Rather than defining health as the absence of illness, health is recognized to be an achievement involving balance among physical, mental, and so- cial well-being. Many use the term wellness to refer
          to this optimum state of health.
          
        </p>
         
        <h4>THE RISE OF THE BIOPSYCHOSOCIAL MODEL
          </h4>

        <p class="preview-text"> 
          The biomedical viewpoint began to change with the rise of modern psychology, particularly with Sigmund Freud’s (1856–1939) early work on conversion hysteria.
          According to Freud, specific unconscious conflicts can
          produce physical disturbances that symbolize repressed psychological conflicts. Although this viewpoint is no longer central to health psychology, it gave rise to the field of psychosomatic medicine.
           
          </p>

          <h4>THE NEED FOR HEALTH PSYCHOLOGY
          </h4>

          <p class="preview-text">
            What factors led to the development of health psy- chology? Since the inception of the field of psychol- ogy in the early 20th century, psychologists have made
 

 
important contributions to health, exploring how and why some people get ill and others do not, how people adjust to their health conditions, and what factors lead people to practice health behaviors. In response to these trends, the American Psychological Association (APA) created a task force in 1973 to focus on psy- chology’s potential role in health research. Partici- pants included counseling, clinical, and rehabilitation psychologists, many of whom were already employed in health settings. Independently, social psycholo- gists, developmental psychologists, and community/ environmental psychologists were developing concep- tual approaches for exploring health issues (Friedman & Silver, 2007). These two groups joined forces, and in 1978, the Division of Health Psychology was formed within the APA. It is safe to say that health psychol- ogy is one of the most important developments within the field of psychology in the past 50 years. What other factors have fueled the growing field of health psychology?

          </p>

        <h4>HEALTH PROMOTION</h4>
        <p class="preview-text"> 
          In recent years, Americans have made substantial gains in altering their poor health habits. Many peo- ple have stopped smoking, and many have reduced their consumption of high-cholesterol and high-fat foods. Coronary heart disease and other chronic dis- eases have shown impressive declines as a result. Al- though alcohol consumption patterns remain largely unchanged, exercise has increased. Despite these ad- vances, obesity is currently endemic and will shortly supplant smoking as the major avoidable contributor to mortality. Poor sleep is an understudied path to poor health (Jarrin, McGrath, & Quon, 2014). Clearly, most people know that they need to practice good health behaviors, and many have tried to de- velop or change them. Not everyone is successful, however.          </p>

